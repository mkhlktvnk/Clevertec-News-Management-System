package ru.clevertec.newsresource.service.message.key;

/**
 * A class containing constants for News Message Keys.
 * These keys are used to retrieve localized error messages for news operations.
 */
public class NewsMessageKey {

    /**
     * The message key for when news is not found by its ID.
     */
    public static final String NOT_FOUND_BY_ID = "news.not-found.by-id";

    /**
     * The message key for when news is unable to be edited.
     */
    public static final String UNABLE_TO_EDIT = "news.unable-to-edit";

    /**
     * The message key for when news is unable to be deleted.
     */
    public static final String UNABLE_TO_DELETE = "news.unable-to-delete";
}

