package ru.clevertec.newsresource.service.message.key;

/**
 * A class containing constants for Comment Message Keys.
 * These keys are used to retrieve localized error messages for comment operations.
 */
public class CommentMessageKey {

    /**
     * The message key for when a comment is not found by its ID.
     */
    public static final String COMMENT_NOT_FOUND_BY_ID = "comment.not-found.by-id";

    /**
     * The message key for when a comment is unable to be edited.
     */
    public static final String UNABLE_TO_EDIT = "comment.unable-to-edit";

    /**
     * The message key for when a comment is unable to be deleted.
     */
    public static final String UNABLE_TO_DELETE = "comment.unable-to-delete";
}

